// ---- Local images ----
// Drop your own photos into public/images/ (see public/images/README.md for the
// exact filenames). Any image you haven't added yet falls back to the gradient
// automatically — the Placeholder hides a missing/broken image on error.
const BASE = (typeof import.meta !== "undefined" && import.meta.env && import.meta.env.BASE_URL) || "/";
export const asset = (p)=> BASE + String(p).replace(/^\//,"");
export const catImg = (id)=> asset(`images/categories/${id}.jpg`);
// Main product photo lives at images/products/<SKU>.jpg (extra shots: <SKU>-2.jpg, <SKU>-3.jpg…).
// We key on the SKU/code — NOT the row id — because ids shift when the price list is rebuilt,
// while the SKU is stable, so a photo you add once keeps matching its product.
export const productImg = (key, n)=> asset(`images/products/${key}${n>1?"-"+n:""}.jpg`);
// gallery: only the images we actually have (no empty placeholder slots).
// p.image may hold one URL or several separated by spaces/commas/newlines.
export const productGallery = (p)=>{
  if(p.image){
    // absolute URLs (supplier) pass through; relative local paths get BASE via asset()
    const urls = String(p.image).split(/[\s,]+/).filter(Boolean)
      .map(u=> /^(https?:|data:|\/\/)/.test(u) ? u : asset(u));
    if(urls.length) return urls;
  }
  return [productImg(p.code || p.key || p.id)]; // single slot (real local file or gradient)
};
